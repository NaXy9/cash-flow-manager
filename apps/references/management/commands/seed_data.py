class Command(BaseCommand):
    help = "Seeds the database with 6 months of realistic demo data."

    def handle(self, *args, **options) -> None:
        self.stdout.write(self.style.HTTP_INFO("▶ Фаза 1 — справочники"))
        refs = self._seed_references()

        self.stdout.write(self.style.HTTP_INFO("\n▶ Фаза 2 — транзакции"))
        self._seed_transactions(refs)

        self.stdout.write(self.style.SUCCESS("\n✓ База готова. http://127.0.0.1:8000\n"))

    # Справочники

    def _seed_references(self) -> dict:
        # Статусы
        statuses: dict[str, Status] = {}
        for name in STATUSES:
            obj, created = Status.objects.get_or_create(name=name)
            statuses[name] = obj
            self._log(created, "Статус", name)

        # Типы
        types: dict[str, TransactionType] = {}
        for name, kind in TYPES.items():
            obj, created = TransactionType.objects.update_or_create(
                name=name, defaults={"kind": kind}
            )
            types[name] = obj
            self._log(created, "Тип", name)

        # Категории и подкатегории
        cats:    dict[tuple, Category]    = {}
        subcats: dict[tuple, Subcategory] = {}

        for type_name, cat_list in CATEGORIES.items():
            t_type = types[type_name]
            for cat_name, sub_names in cat_list:
                cat, created = Category.objects.get_or_create(
                    name=cat_name, transaction_type=t_type
                )
                cats[(type_name, cat_name)] = cat
                self._log(created, "Категория", f"{cat_name} → {type_name}")

                for sub_name in sub_names:
                    sub, created = Subcategory.objects.get_or_create(
                        name=sub_name, category=cat
                    )
                    subcats[(cat_name, sub_name)] = sub
                    self._log(created, "  Подкатегория", sub_name)

        return {
            "statuses": statuses,
            "types":    types,
            "cats":     cats,
            "subcats":  subcats,
        }

